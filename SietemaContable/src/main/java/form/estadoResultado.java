    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        JPanel panel = new JPanel();
        panel.setLayout(new BoxLayout(panel, BoxLayout.Y_AXIS)); // Layout vertical
        
        JLabel label1 = new JLabel("Utilidad Bruta:");
        JTextField campoBruta = new JTextField(15);
        
        JLabel label2 = new JLabel("Utilidad Operativa:");
        JTextField campoOperativa = new JTextField(15);
        
        JLabel label4 = new JLabel("Utilidad antes de impuestos:");
        JTextField campoImpuesto = new JTextField(15);
        
        JLabel label5 = new JLabel("Utilidad Neta:");
        JTextField campoNeta = new JTextField(15);
        
        FiltroNumeros filtro = new FiltroNumeros();
        
        ((AbstractDocument) campoBruta.getDocument()).setDocumentFilter(filtro);
        ((AbstractDocument) campoOperativa.getDocument()).setDocumentFilter(filtro);
        ((AbstractDocument) campoImpuesto.getDocument()).setDocumentFilter(filtro);
        ((AbstractDocument) campoNeta.getDocument()).setDocumentFilter(filtro);
        
        if(resultado != null){
            campoBruta.setText(String.valueOf(resultado.getUtilidad_bruta()));
            campoOperativa.setText(String.valueOf(resultado.getUtilidad_operativa()));
            campoImpuesto.setText(String.valueOf(resultado.getUtilidad_antes_impuestos()));
            campoNeta.setText(String.valueOf(resultado.getUtilidad_neta()));
        }
        
        panel.add(label1);
        panel.add(campoBruta);
        panel.add(label2);
        panel.add(campoOperativa);
        panel.add(label4);
        panel.add(campoImpuesto);
        panel.add(label5);
        panel.add(campoNeta);
        
        int result = JOptionPane.showConfirmDialog(null, panel, "Modificar Estado de Resultado", JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);
        if (result == JOptionPane.OK_OPTION) {
            if (!campoBruta.getText().equals("") && !campoOperativa.getText().equals("") && !campoImpuesto.getText().equals("") && !campoNeta.getText().equals("")) {
                EstadoResultado estadoModificado = new EstadoResultado(Double.parseDouble(campoBruta.getText()),Double.parseDouble(campoOperativa.getText()),
                    Double.parseDouble(campoImpuesto.getText()), Double.parseDouble(campoNeta.getText()), empresa_id);
            
                if (resultado != null) {
                    
                    catalogoCuentas.modificarEstadoResultado(estadoModificado);
                    cargarEstado();
                }else{
                    System.out.println("llega a anbtes de insertar");
                    catalogoCuentas.insertarEstadoResultado(estadoModificado);
                    cargarEstado();
                }
            }else{
                JOptionPane.showMessageDialog(null, "No deje campos vacios", "Informacion", JOptionPane.QUESTION_MESSAGE);
            }
            
        }
        
    }//GEN-LAST:event_btnActualizarActionPerformed


